package com.raido.task2.type;

/**
 * This enum provides the list of all types
 * of text elements that make up basic
 * terminology adopted in the application.
 */

public enum ElementType {

    /**
     * The following elements are considered
     * compound:
     */

    TEXT,
    PARAGRAPH,
    SENTENCE,

    /**
     * The following elements are considered
     * primitive:
     */

    LEXEME,
    WORD,
    PUNCTUATION_SIGN
}
